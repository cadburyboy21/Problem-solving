public class AssignmentOperator {
    public static void main(String[] args){
        int Num=10;
        
        // Addition
        Num +=5;

        // Subraction
        Num -=5;

        // Multiplication
        Num *=5;

        //Division
        Num /=5;

        //To find the Reminder
        Num %=5;

        System.out.println(Num);
    
    }
}
